export async function seedGajiPokok() {
  const records = RAW.map(([golonganId, masaKerja, gajiPokok]) => ({
    golonganKode: GOLONGAN_ID_TO_KODE[golonganId],
    masaKerja,
    gajiPokok: new Prisma.Decimal(gajiPokok),
    berlakuSejak: new Date('2024-01-01'),
  }));

  // Upsert in chunks of 100
  let total = 0;
  for (let i = 0; i < records.length; i += 100) {
    const chunk = records.slice(i, i + 100);
    await Promise.all(
      chunk.map(r =>
        prisma.refGajiPokok.upsert({
          where: { golonganKode_masaKerja_berlakuSejak: { golonganKode: r.golonganKode, masaKerja: r.masaKerja, berlakuSejak: r.berlakuSejak } },
          create: r,
          update: { gajiPokok: r.gajiPokok, berlakuSejak: r.berlakuSejak },
        }),
      ),
    );
    total += chunk.length;
  }
  console.log(`  ✓ RefGajiPokok: ${total} records upserted`);
}

// Allow running standalone
if (require.main === module) {
  seedGajiPokok()
    .then(() => prisma.$disconnect())
    .catch(async (e) => {
      console.error(e);
      await prisma.$disconnect();
      process.exit(1);
    });
}
